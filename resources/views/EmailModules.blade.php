<h2> Communication </h2>
<h3>Communication Essentials</h3>
<ul>
                  <li> Know your doctor’s name and the phone number for their office.</li>
                  <li> If you see more than one doctor, understand what concern does each doctor address. </li>
                  <li> For your next visit, ask your parent/guardian to let you call the doctor’s office and set up the appointment.  </li>
                  <li> At the appointment, ask your parent/guardian to let you talk with the doctor.</li>
                  <li> Ask questions about your health, medication and anything else you would like to know.</li>
                  <li> Learn how to reach out to transportation. Try calling 
                    in for transportation yourself at the next doctor’s visit.</li>    
                  <li> Make a note of all the above information in your diary.</li>
                </ul>
<br>
<h3>Transition</h3>
<ul>
                  <li>Did you know that your current pediatric doctor can see you until you reach the age of 18? 
                    After that, you would be seen by an adult doctor.</li>
                  <li>Do you or your parents know who you would see? </li>
                  <li>If you have any concerns about seeing the adult doctor, discuss it with your current doctor.</li> 
                  <li>If you need help choosing an adult doctor, ask your current doctor for recommendations.</li>   
                </ul>
<br>
<br>
<h2> Self Care </h2>
<h3>Taking care of yourself</h3>
<ul>
                  <li> Ask you parent and your doctor if you have restriction for any food or activity.</li>
                  <li>Remember your allergies!</li>
                <li>Learn to put together a simple meal for yourself.</li>
                <li>Try doing the daily chores by yourself.</li>
</ul>
<br>
<h3>Taking Medications</h3>
<ul>
            <li>Make a list of your medications, dose and frequency (how many times in a day).</li>
            <li>Take your medications yourself! Try to remember and avoid using reminders for medications. </li>
            <li>Ask your parent what pharmacy you use and make a note in your diary.</li>
            <li>Try to refill your own medication at the pharmacy next time.</li>
                </ul>
<h3>Transportation</h3>
<ul>
<li>Learn how to contact the transportation.</li>
<li>At the next doctor’s visit, try calling in for transportation yourself.</li>
                </ul>
                <br>
<h2> Health Awareness </h2>
<h3>Medical History</h3>
<ul>
                    <li> Ask your parents the following: </li>
                    <li>  What is your medical condition?</li> 
                    <li> How old were you when you first had the illness?</li> 
                    <li> How many times have you been admitted to the hospital?</li> 
                    <li> Have you had any surgeries? When and what kind?</li> 
                    <li> Do you have any implanted prosthetics or devices in your body? If yes, ask your parent if they have the details about the device?</li> 
                    <li> Any restrictions because of your illness.</li> 
                    <li> Make a note of these in your diary. </li> 

                </ul>
<br>
<h3>Medications</h3>
<ul>
       <li> Make a list of your medications, dose and frequency, i.e. how many times in a day do you have to take it.</li>
       <li> Ask your parents or doctor why you are taking each of these medications. </li>
       <li> Know if you are allergic to any medication.</li>
       <li> Ask your doctor about side effects and what should you do if you have one?</li>
       <li> Ask your doctor if there are any potential interactions with other medications and anything that you should avoid?</li>
       <li> Keep the list updated.</li>
       <li> Ask your doctor about the need for antibiotics before procedures such as dental work.</li>

                </ul>
                <br>
<h3>Insurance</h3>
<ul>
            <li>Ask the following:</li>
            <li> What type of insurance do you have? Are you covered under your parents’ insurance plan or an independent plan?</li>
            <li>Is there a premium and who pays for it?</li>
            <li>When do you need to renew or re-apply for your insurance? Try doing the paperwork yourself at the next renewal.</li>
            <li>Who has your insurance card? If you don’t, please ask your parents for it and have it with you at all times.</li>

                </ul>
                <br>
<h3>What to do when I feel sick?</h3>
<ul>
       <li> Know the contact information for your doctor’s office.</li>
       <li> Learn how to contact transportation in case you need to visit the doctor or the emergency room.</li>
       <li> Ask your doctor if you have any rescue medications and how to take them.</li>

                </ul>
