      Welcome to My Transition Explorer. We hope you enjoy using this application.
      Below are the Modules!
      <br><br>
      <p>Thank you for choosing My Transition Explorer to guide you or 
         your child, in the transition from pediatric care to adult care.
         This page is benchmarks of transition process based on age.
      </p>
      <br>         
      <h1> Ages 13-15 </h1>
      <h3 >Self-Care</h3>
      <p>Start taking medication by yourself without reminders </p>
      <p>Learn to be comfortable taking care of yourself</p>
      <br>
      <h3 >Health Awareness</h3>
      <p>Learn about your medical conditions and medications</p>
      <p>Understand what complications may come from your medical conditions</p>
      <br>
      <h3 >Communication</h3>
      <p>Understand that you will see an adult doctor in the next few years </p>
      <p> Start to speak with the doctor by yourself without help from family
          or friends</p>
      <br>
      <br>
      <h1> Ages 15-17 </h1>
      <h3 >Self-Care</h3>
      <p>Be able to take medication by yourself without any help  </p>
      <p>Feel comfortable taking care of yourself</p>
      <br>
      <h3 >Health Awareness</h3>
      <p>Know your complete medical history and medications </p>
      <p>Know what to do and who to talk to when you feel ill  </p>
      <br>
      <h3 >Communication</h3>
      <p>Be able to talk to your doctors and nurses by yourself </p>
      <p> Know how to call the doctor's office if you feel ill  </p>
      <br>
      <h1> Ages 17-19 </h1>
      <h3 >Self-Care</h3>
      <p>Know how to get your medications refilled  </p>
      <p>Know how to arrange for transportation to your doctor's appointment</p>
      <br>
      <h3 >Health Awareness</h3>
      <p>Learn what type of insurance you have and how to apply for it  </p>
      <p>Know which doctors you need to see and why you are seeing them </p>
      <br>
      <h3 >Communication</h3>
      <p>Feel prepared to transition from your current doctor to an adult doctor</p>
      <p> Be able to schedule doctor's appointments by yourself </p>
      <br>
